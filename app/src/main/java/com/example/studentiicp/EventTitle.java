package com.example.studentiicp;

public class EventTitle {
    String title;
    String type;
    String id;
    int dayOfMonth, month, year, hourOfDay, minute;

    public String getTitle() {
        return title;
    }

    public String getType() {
        return type;
    }

    public String getId() {
        return id;
    }

    public int getDayOfMonth() {
        return dayOfMonth;
    }

    public int getMonth() {
        return month;
    }

    public int getYear() {
        return year;
    }

    public int getHourOfDay() {
        return hourOfDay;
    }

    public int getMinute() {
        return minute;
    }
}